package net.merise.platform.dao.estate.pojo;

import java.util.Date;

/**
 * 报事报修信息记录表
 * @author Administrator
 *
 */
public class NewspaperReport {
	
	//报事报修编号
	private String newspaper_id;
	//报事报修类别（报事、报修、建议）
	private String newspaper_type;
	//上报方式（电话、来访、app、其他）
	private String report_type;
	//事件名称
	private String newspaper_name;
	//事件内容
	private String newspaper_content;
	//事件上报时间
	private Date newspaper_time;
	//事件状态
	private String newspaper_status;
	//事件备注
	private String newspaper_remark;
	//报事报修人编号（业主编号）
	private String owner_id;
	//房屋编号
	private String house_id;
	//归档时间
	private Date end_time;
	
	
	private String wctimes;//前后相差时间 小时
	
	public String getNewspaper_id() {
		return newspaper_id;
	}
	public void setNewspaper_id(String newspaper_id) {
		this.newspaper_id = newspaper_id;
	}
	public String getNewspaper_type() {
		return newspaper_type;
	}
	public void setNewspaper_type(String newspaper_type) {
		this.newspaper_type = newspaper_type;
	}
	public String getReport_type() {
		return report_type;
	}
	public void setReport_type(String report_type) {
		this.report_type = report_type;
	}
	public String getNewspaper_name() {
		return newspaper_name;
	}
	public void setNewspaper_name(String newspaper_name) {
		this.newspaper_name = newspaper_name;
	}
	public String getNewspaper_content() {
		return newspaper_content;
	}
	public void setNewspaper_content(String newspaper_content) {
		this.newspaper_content = newspaper_content;
	}
	public Date getNewspaper_time() {
		return newspaper_time;
	}
	public void setNewspaper_time(Date newspaper_time) {
		this.newspaper_time = newspaper_time;
	}
	public String getNewspaper_status() {
		return newspaper_status;
	}
	public void setNewspaper_status(String newspaper_status) {
		this.newspaper_status = newspaper_status;
	}
	public String getNewspaper_remark() {
		return newspaper_remark;
	}
	public void setNewspaper_remark(String newspaper_remark) {
		this.newspaper_remark = newspaper_remark;
	}
	public String getOwner_id() {
		return owner_id;
	}
	public void setOwner_id(String owner_id) {
		this.owner_id = owner_id;
	}
	public String getHouse_id() {
		return house_id;
	}
	public void setHouse_id(String house_id) {
		this.house_id = house_id;
	}
	public String getWctimes() {
		return wctimes;
	}
	public void setWctimes(String wctimes) {
		this.wctimes = wctimes;
	}
	public Date getEnd_time() {
		return end_time;
	}
	public void setEnd_time(Date end_time) {
		this.end_time = end_time;
	}
	
	
}

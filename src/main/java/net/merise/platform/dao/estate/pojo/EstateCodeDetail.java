package net.merise.platform.dao.estate.pojo;

public class EstateCodeDetail {

	private String code_id;
	private String building_id;
	private String code_name;
	private String code_content;
	private int code_sequence;
	private String code_remark;

	private String building_name;
	private String estate_name;

	public void setCode_id(String code_id) {
		this.code_id = code_id;
	}

	public String getCode_id() {
		return code_id;
	}

	public void setBuilding_id(String building_id) {
		this.building_id = building_id;
	}

	public String getBuilding_id() {
		return building_id;
	}

	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}

	public String getCode_name() {
		return code_name;
	}

	public void setCode_content(String code_content) {
		this.code_content = code_content;
	}

	public String getCode_content() {
		return code_content;
	}

	public void setCode_sequence(int code_sequence) {
		this.code_sequence = code_sequence;
	}

	public int getCode_sequence() {
		return code_sequence;
	}

	public void setCode_remark(String code_remark) {
		this.code_remark = code_remark;
	}

	public String getCode_remark() {
		return code_remark;
	}

	public String getBuilding_name() {
		return building_name;
	}

	public void setBuilding_name(String building_name) {
		this.building_name = building_name;
	}

	public String getEstate_name() {
		return estate_name;
	}

	public void setEstate_name(String estate_name) {
		this.estate_name = estate_name;
	}
}

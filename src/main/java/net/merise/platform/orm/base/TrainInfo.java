package net.merise.platform.orm.base;

import java.util.Date;

/**
 * 培训信息表
 * @author Administrator
 *
 */
public class TrainInfo {
	
	//培训项目编号
	private String train_id;
	//培训项目名称
	private String train_name;
	//培训项目题目
	private String train_info_name;
	//培训项目内容
	private String train_content;
	//培训项目时间
	private Date train_time;
	//培训项目状态
	private String train_status;
	//培训项目备注
	private String train_remark;
	//物业编号
	private String estate_id;
	//物业名称
	private String estate_name;
	
	public String getTrain_id() {
		return train_id;
	}
	public void setTrain_id(String train_id) {
		this.train_id = train_id;
	}
	public String getTrain_name() {
		return train_name;
	}
	public void setTrain_name(String train_name) {
		this.train_name = train_name;
	}
	public String getTrain_info_name() {
		return train_info_name;
	}
	public void setTrain_info_name(String train_info_name) {
		this.train_info_name = train_info_name;
	}
	public String getTrain_content() {
		return train_content;
	}
	public void setTrain_content(String train_content) {
		this.train_content = train_content;
	}
	public Date getTrain_time() {
		return train_time;
	}
	public void setTrain_time(Date train_time) {
		this.train_time = train_time;
	}
	public String getTrain_status() {
		return train_status;
	}
	public void setTrain_status(String train_status) {
		this.train_status = train_status;
	}
	public String getTrain_remark() {
		return train_remark;
	}
	public void setTrain_remark(String train_remark) {
		this.train_remark = train_remark;
	}
	public String getEstate_id() {
		return estate_id;
	}
	public void setEstate_id(String estate_id) {
		this.estate_id = estate_id;
	}
	public String getEstate_name() {
		return estate_name;
	}
	public void setEstate_name(String estate_name) {
		this.estate_name = estate_name;
	}
}
